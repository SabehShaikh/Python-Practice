# Integer: An integer is a whole number, positive or negative, without decimals
age = 20
print("My age is ", age)
print(type(age))

# Float: A float is a number, positive or negative, containing one or more decimals
price = 40.99
print("price is ", price)
print(type(price))

# String: A string is a sequence of characters
name = "Sabeh Shaikh"
print("My name is ", name)
print(type(name))

# Boolean: A boolean is a data type that can only be True or False
is_adult = True
print("Am I an adult? ", is_adult)
print(type(is_adult))

# Range
numbers = range(1, 10)
print("Numbers from 1 to 9: ", numbers)
print(type(numbers))
