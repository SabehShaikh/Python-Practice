
# Arithmetic Operators
x = 10
y = 5

print(f"Addition: {x} + {y} = {x + y}")
print(f"Subtraction: {x} - {y} = {x -y}")
print(f"Multiplication: {x} * {y} = {x * y}")
print(f"Division: {x} / {y} = {x / y}")
print(f"Modulo: {x} % {y} = {x % y}")
print(f"Exponent: {x} ** {y} = {x ** y}")
print(f"Floor Division: {x} // {y} = {x // y}")

# Comparison Operators
x = 20
y = 30

print(f"\nIs {x} equal to {y}? {x == y}")
print(f"Is {x} not equal to {y}? {x != y}")
print(f"Is {x} greater than {y}? {x > y}")
print(f"Is {x} less than {y}? {x < y}")
print(f"Is {x} greater than or equal to {y}? {x >= y}")
print(f"Is {x} less than or equal to {y}? {x <= y}")

# Assignment Operators
a = 10
a += 5
print(f"a += 5: {a}") 

a -= 8
print(f"a -= 3: {a}")

a *= 7
print(f"a *= 2: {a}")

a /= 3 
print(f"a /= 3: {a}")

a %= 4
print(f"a %= 4: {a}")

a **= 2
print(f"a **= 2: {a}")

a //= 3
print(f"a //= 3: {a}")

# Logical Operators
c = 15
d = 20

print(f"Is {c} greater than 10 and {d} less than 30? {c > 10 and d < 30}")
print(f"Is {c} greater than 10 or {d} less than 30? {c > 10 or d < 30}")
print(f"Is not {c} greater than 10? {not c > 10}")



